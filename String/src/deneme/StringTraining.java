package deneme;

public class StringTraining {

	public static void main(String[] args) {

		int sayi = 123;
		String str = "Çağatay Sarıoğlan";
		double dob = 1.324403;

		System.out.println(sayi + "-" + str + "-" + dob);

		System.out.printf("sayı: %d, str: %.10S, double: %f", sayi, str, dob);
	}

}
